package Core;

public class Force {
    public float x = 0;
    public float y = 0;
    public float forceX = 0;
    public float forceY = 0;
    
    public Force(float x, float y, float forceX, float floatY) {
        this.x = x;
        this.y = y;
        this.forceX = forceX;
        this.forceY = forceY;
    }
}
